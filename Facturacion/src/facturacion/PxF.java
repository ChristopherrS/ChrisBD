
package facturacion;

public class PxF {
    private String facNumero;
    private String proCodigo;
    private double pxfCantidad;
    private double pxfValor;
    private double pxfSubtotal;
    private String pxfStatus;

    public String getFacNumero() {
        return facNumero;
    }

    public void setFacNumero(String facNumero) {
        this.facNumero = facNumero;
    }

    public String getProCodigo() {
        return proCodigo;
    }

    public void setProCodigo(String proCodigo) {
        this.proCodigo = proCodigo;
    }

    public double getPxfCantidad() {
        return pxfCantidad;
    }

    public void setPxfCantidad(double pxfCantidad) {
        this.pxfCantidad = pxfCantidad;
    }

    public double getPxfValor() {
        return pxfValor;
    }

    public void setPxfValor(double pxfValor) {
        this.pxfValor = pxfValor;
    }

    public double getPxfSubtotal() {
        return pxfSubtotal;
    }

    public void setPxfSubtotal(double pxfSubtotal) {
        this.pxfSubtotal = pxfSubtotal;
    }

    public String getPxfStatus() {
        return pxfStatus;
    }

    public void setPxfStatus(String pxfStatus) {
        this.pxfStatus = pxfStatus;
    }

    
}
